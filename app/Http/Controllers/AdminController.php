    public function auth(Request $request)
    {
        $validate = $request->validate([
            'email' => 'required|email',
            'password' => 'required|min:8'
        ]);

        $admin = Admin::where('email', $validate['email'])->first();

        if ($admin && $validate['password'] == $admin->password) {
            // Authentication successful, redirect to the admin dashboard
            return redirect('admin-dashboard');
        } else {
            // Authentication failed, redirect back with an error message
            return redirect('/admin-login')->with('error', 'Invalid email or password')
                ->withInput(); // Preserve input values on redirect
        }
    }


